let cor = "verde";

/*Estrutura que seleciona (escolhe), quais comandos serão executados baseados em cada condição 
especificada*/
switch (cor) {

    case "verde":
        console.log("siga");
    break;

    case "amarelo":
        console.log("atenção");
    break;

    case "vermelho":
        console.log("pare");
    break;

    default:
        console.log("não sei");
}